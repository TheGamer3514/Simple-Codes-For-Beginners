/*
 * if_else.js
 * This script demonstrates if-else conditions by checking if a number is positive, negative, or zero.
 */

let number = -10;
if (number > 0) {
    console.log("The number is positive.");
} else if (number < 0) {
    console.log("The number is negative.");
} else {
    console.log("The number is zero.");
}
