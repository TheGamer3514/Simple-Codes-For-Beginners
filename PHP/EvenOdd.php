<?php
/*
 * EvenOdd.php
 * This script checks if a number is even or odd and displays the result.
 */

// Define a number
$number = 7; // Change this number to test different values

// Check if the number is even or odd
if ($number % 2 == 0) {
    echo $number . " is even.";
} else {
    echo $number . " is odd.";
}
?>
